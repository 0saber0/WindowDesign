package com.milanuo.window.util;

/**
 * C型窗户尺寸
 * 
 * @author 51988
 *
 */
public class CTypeHigh {
	private Integer topHigh;
	private Integer midHigh;
	private Integer downHigh;

	public Integer getTopHigh() {
		return topHigh;
	}

	public void setTopHigh(Integer topHigh) {
		this.topHigh = topHigh;
	}

	public Integer getMidHigh() {
		return midHigh;
	}

	public void setMidHigh(Integer midHigh) {
		this.midHigh = midHigh;
	}

	public Integer getDownHigh() {
		return downHigh;
	}

	public void setDownHigh(Integer downHigh) {
		this.downHigh = downHigh;
	}

}
