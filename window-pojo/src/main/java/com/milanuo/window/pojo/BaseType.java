package com.milanuo.window.pojo;

/**
 * 封装所有基础数据
 * 
 * @author 51988
 *
 */
public class BaseType {

	private Integer iH;// 输入高
	private Integer iW;// 输入宽
	private Integer iN;// 输入数量
	private Integer tH;// 上高
	private Integer mH;// 中高
	private Integer dH;// 下高

	public Integer getiH() {
		return iH;
	}

	public void setiH(Integer iH) {
		this.iH = iH;
	}

	public Integer getiW() {
		return iW;
	}

	public void setiW(Integer iW) {
		this.iW = iW;
	}

	public Integer getiN() {
		return iN;
	}

	public void setiN(Integer iN) {
		this.iN = iN;
	}

	public Integer gettH() {
		return tH;
	}

	public void settH(Integer tH) {
		this.tH = tH;
	}

	public Integer getmH() {
		return mH;
	}

	public void setmH(Integer mH) {
		this.mH = mH;
	}

	public Integer getdH() {
		return dH;
	}

	public void setdH(Integer dH) {
		this.dH = dH;
	}

}
